  const langElements = document.querySelectorAll('.lang');
  
  function changeLanguage(language) {
    selectedLanguage = language;
    langElements.forEach(el => {
      let text = langData[selectedLanguage][el.getAttribute('key')];
      if (el.tagName === 'P') {
        text = text.replace(/\\/g, '').replace(/\n/g, '<br>'); // Bu qatorni qo'shish
        el.innerHTML = text;
      } else {
        el.textContent = text;
      }
    });
  }
  
  
  
  
